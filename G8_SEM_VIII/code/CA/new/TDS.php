    <h2>Tax Deducated tax (TDS)</h2><br>
    <h3>Introduction</h3>
    <p>The concept of TDS was introduced with an aim to collect tax from the very source of income. As per this concept, a person (deductor) who is liable to make payment of specified nature to any other person (deductee) shall deduct tax at source and remit the same into the account of the Central Government. The deductee from whose income tax has been deducted at source would be entitled to get credit of the amount so deducted on the basis of Form 26AS or TDS certificate issued by the deductor.</p>

    <h3>Rates for deduct of tax at source</h3>
    <p>Taxes shall be deducted at the rates specified in the relevant provisions of the Act or the First Schedule to the Finance Act. However, in case of payment to non-resident persons, the withholding tax rates specified under the Double Taxation Avoidance Agreements shall also be considered</p>

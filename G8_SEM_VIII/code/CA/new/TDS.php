    <h3>How to pay Tax Deducted/Collected at source?</h3>
    <p>Tax deducted or collected at source shall be deposited to the credit of the Central Government by following modes:
        1) Electronic mode: E-Payment is mandatory for
        a) All corporate assesses; and
        b) All assesses (other than company) to whom provisions of section 44AB of the Income Tax Act, 1961 are applicable.
        2) Physical Mode: By furnishing the Challan 281 in the authorized bank branch</p>
